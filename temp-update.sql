## 3. デジタル印刷のメリット

### メリット1: 版を作らない

最大のメリットは、版を作成する必要がない点です。

- **初期費用0円**: 版代がかからない
- **デザイン変更自由**: デザインの修正も追加費用なし
- **短納期**: 版作成のリードタイムなし

### メリット2: 500枚から製造可能

業界最小クラスの小ロット対応です。

| 数量 | グラビア印刷 | デジタル印刷 | 推奨方式 |
|------|--------------|--------------|----------|
| 500枚 | 不可 | 可能 | デジタル印刷 |
| 1,000枚 | 不可 | 可能 | デジタル印刷 |
| 5,000枚 | 検討可 | 可能 | デジタル印刷 |
| 10,000枚 | 可能 | 可能 | デジタル印刷（推奨） |
| 20,000枚 | 可能 | 可能 | 価格はほぼ同じ |
| 30,000枚以上 | 可能 | 可能 | グラビア印刷（コスト優位） |

→ **テスト販売、A/Bテストに最適**

### メリット3: 多品種小ロットに対応

複数のデザインを少量ずつ製造可能です。

- **フレーバー違い**: 3種類×100枚ずつ
- **地域限定**: 店舗別のデザイン
- **テストマーケティング**: 販売結果に応じて本格生産

### メリット4: データ更新が容易

デザインの修正・更新が簡単です。

- 価格変更
- 成分表示の更新
- 法規制対応

