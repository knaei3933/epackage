## 1. デジタル印刷とは

デジタル印刷は、デジタルデータを直接印刷機に送って印刷する方式です。紙の印刷でいうインクジェットプリンターと同じ仕組みです。

### 基本概念

- **デジタル印刷**: データを直接印刷機に送り、インクを吹き付ける方式
- **版不要**: グラビア印刷のような版を作成する必要がない
- **On-Demand印刷**: 必要な枚数を必要な時に印刷可能

### Package-Labのデジタル印刷専門性

Package-Labはデジタル印刷に特化しています。

| 特徴 | 説明 |
|------|------|
| **最小ロット** | 500枚から対応可能（業界最小クラス） |
| **短納期** | 最短21日、平均28日で納品 |
| **版代無料** | 初期費用0円 |
| **高品質** | 最新機種でグラビアに迫る品質 |

