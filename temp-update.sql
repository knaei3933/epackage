## よくある質問

### Q: デジタル印刷とグラビア印刷、どっちが得？
A: 20,000枚未満ならデジタル印刷が圧倒的に安く、30,000枚以上ならグラビア印刷がコスト優位になります。20,000〜30,000枚は境界域で、納期やデザイン変更の有無で選びます。

### Q: 最小発注数は？
A: 500枚から発注可能です。業界最小クラスの小ロット対応です。

### Q: 版代はかかりますか？
A: デジタル印刷は版代0円です。グラビア印刷は版代として12万円程度（4色×3万円/色）かかります。

### Q: 納期はどのくらい？
A: デジタル印刷は最短21日、平均28日で納品可能です。グラビア印刷は30〜45日かかります。

### Q: 見積もりは会員登録が必要ですか？
A: 価格確認のみなら会員登録不要でOKです。見積を保存する場合のみ会員登録（無料）が必要です。

