## 2. グラビア印刷との違い

### 方式の比較

| 項目 | デジタル印刷 | グラビア印刷 |
|------|-------------|--------------|
| **版の要否** | 不要（0円） | 必要（10万〜50万円） |
| **最小ロット** | 500枚〜 | 3,000〜5,000枚〜 |
| **納期** | 21〜28日 | 30〜45日 |
| **初期費用** | なし | 版代が必要 |
| **単価** | 小ロットで有利 | 大量で有利 |
| **色再現性** | 優秀（最新機） | 最も優秀 |
| **デザイン変更** | 容易 | 困難（版作り直し） |

### 費用比較イメージ

**1,000枚のパッケージ製造の場合:**

| 費用項目 | デジタル印刷 | グラビア印刷 |
|----------|-------------|--------------|
| **初期費用（版代）** | 0円 | 12万円（4色×3万円/色） |
| **製造単価** | 150円/枚 | 120円/枚 |
| **総額** | 15万円 | 27万円 |

→ **1,000枚以下ならデジタル印刷が圧倒的に安い**

**20,000枚のパッケージ製造の場合:**

| 費用項目 | デジタル印刷 | グラビア印刷 |
|----------|-------------|--------------|
| **初期費用（版代）** | 0円 | 12万円（4色×3万円/色） |
| **製造単価** | 30円/枚 | 30円/枚 |
| **総額** | 60万円 | 72万円 |

→ **20,000枚では価格はほぼ同じ**

**30,000枚以上でグラビア印刷がコスト優位**: 版代が回収され、単価のメリットが大きくなります。

**注意**: グラビア印刷は一般的に10,000枚以上の製造で検討されます。10,000枚未満では版代のコストパフォーマンスが悪くなります。

