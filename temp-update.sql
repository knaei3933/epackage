## 5. 発注方法

Package-Labでのデジタル印刷発注は、以下の流れで進めます。

### STEP1: スマート見積りで条件入力

まずは見積もりシステムで基本条件を入力します。

- パッケージタイプ（スタンドパウチ、平袋など）
- サイズ（幅×高さ（マチ））
- 素材（PET/AL/PE、PET/VMPET/PEなど）
- 印刷色数（1色〜10色）
- 数量（500枚〜）

### STEP2: 見積内容の確認

入力完了後、即座に以下の情報が表示されます。

- 製造単価
- 合計金額
- 納期目安（最短21日、平均28日）
- 費用の内訳

### STEP3: 見積の保存（会員登録）

見積を保存して続きをする場合は、会員登録（無料）が必要です。

- 価格確認のみなら会員登録不要
- 見積履歴の保存・管理には会員登録が必要

### STEP4: デザインデータ入稿

デザインデータをアップロードします。

#### データ仕様

| 項目 | 推奨仕様 |
|------|----------|
| **フォーマット** | PDF, AI形式 |
| **カラーモード** | CMYK |
| **解像度** | 300dpi以上 |
| **サイズ** | 実寸プラス塗り足し3mm |

#### デザインがない場合

- デザインデータはお客様ご用意ください
- テンプレートも提供しております

### STEP5: データチェック・製造

入稿されたデータをチェックし、製造〜納品となります。

- データチェック（無料）
- デジタルproofの確認
- 実機見本の郵送（オプション）
- 製造（最短21日、平均28日）
- 納品

