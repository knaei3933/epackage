## 1. 平袋とは

平袋（ひらぶくろ）は、最も基本的な形状のパウチです。前面と背面の2つの平面で構成され、側面や底部の折り返しがないシンプルなデザインです。3面シールとも呼ばれ、封口が前面・背面・底面の3面になっているのが特徴です。

### 基本構造

| 部位 | 説明 |
|------|------|
| **前面** | ブランドロゴや商品情報を印刷するメイン面 |
| **背面** | 成分表示、使用方法、会社情報などを記載 |
| **チャック** | 開閉可能なジッパー（オプション） |
| **シール部分** | 3面をシールで閉じる |

---

## 2. 平袋の特徴と種類

### 3面シールの特徴

平袋の最大の特徴は「3面シール」です。

- **前面シール**: 商品を入れ、前面で封をする
- **背面シール**: 背面で封をして、袋を完成させる
- **底面シール**: 底部をシールで閉じる

このシール方式により、シンプルながらもしっかりとした密封性を実現しています。

### サイズの種類

平袋はサイズの選択肢が豊富です。

| サイズ | 容量目安 | 適した商品 |
|--------|----------|-----------|
| **小（100×150mm以下）** | 50g以下 | 試供品、サンプル |
| **中（100×200mm程度）** | 50〜200g | ナッツ、スパイス |
| **大（150×250mm以上）** | 200g以上 | クッキー、ドライフルーツ |

---

## 3. メリット・デメリット

### メリット1: コストパフォーマンスが最強

平袋は、パッケージの中でも最も安価な選択肢です。

- 製造コストが低い
- 印刷も比較的安価
- 小ロットでもコスト面で優位

### メリット2: シンプルで使いやすい

複雑な形状ではないため、使い勝手が良いです。

- 開けやすく、使いやすい
- ストックしやすい
- 消費者にも親しみやすい

### デメリット

| デメリット | 説明 | 対策 |
|-----------|------|------|
| **自立しない** | 袋が自立しない | スタンドパウチやガゼットパウチを検討 |
| **容量が限定的** | 大容量には不向き | 大容量の商品の場合は他のタイプを選択 |
| **陳列効果が低い** | 店頭での訴求力は低い | デザインで目立たせる工夫が必要 |

